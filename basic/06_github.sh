# Repository
Create Repository at github website.

# Clone
## new-name is optional
$ git clone https://github.com/cygnu7/belajar-git.git [new-name]

# Push
$ git push origin master

# Pull
$ git pull

# Branch
$ git checkout -b [branch-name]
## Delete Branch
$ git branch -d [branch-name]

# Merge
## Do it on branch master
$ git merge [branch-name]

